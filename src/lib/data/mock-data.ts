// --- Helper Functions ---

export function getAlumniById(id: string): Alumni | undefined {
  return alumni.find((a) => a.id === id);
}

export function getAlumniByBranch(branch: string): Alumni[] {
  return alumni.filter((a) => a.branch === branch);
}

export function getAlumniByCompany(company: string): Alumni[] {
  return alumni.filter((a) => a.currentCompany.toLowerCase().includes(company.toLowerCase()));
}

export function getAlumniByDomain(domain: string): Alumni[] {
  return alumni.filter((a) => a.domain === domain);
}

export function getAvailableMentors(): Alumni[] {
  return alumni.filter((a) => a.availability === "available" && a.verificationStatus === "verified");
}

export function getForumPostsByCategory(category: string): ForumPost[] {
  if (category === "All") return forumPosts;
  return forumPosts.filter((p) => p.category === category);
}

export function getOpportunitiesByType(type: string): Opportunity[] {
  if (type === "All") return opportunities;
  return opportunities.filter((o) => o.type === type);
}

export function getStoriesByCategory(category: string): AlumniStory[] {
  if (category === "All") return alumniStories;
  return alumniStories.filter((s) => s.category === category);
}

export function getFeaturedStories(): AlumniStory[] {
  return alumniStories.filter((s) => s.featured);
}

export function getUpcomingEvents(): PlatformEvent[] {
  const now = new Date();
  return events.filter((e) => new Date(e.date) >= now).sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime());
}

export function getResourcesByCategory(category: string): Resource[] {
  if (category === "All") return resources;
  return resources.filter((r) => r.category === category);
}
